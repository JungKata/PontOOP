package hu.petrik.PontOOP.Kor;

public class Kor {
    private double s;
    private int x;
    private int y;


    public Kor(double s) {
    this.s = s;
    x = 0;
    y = 0;
    }

    public Kor(double s, int x, int y)
    {
        this.s = s;
        this.x = x;
        this.y = y;
    }



}